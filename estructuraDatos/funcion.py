##escriba una funcion en python que acepte una lista y 
# genere otra lista eliminando los duplicados
def delDuplicate(array):
    newArray=[]
    for el in array:
        if el not in newArray:
           newArray.append(el)
    return newArray
arrayD=[1,1,2,4,5,5,8,8,6,6]
print(delDuplicate(arrayD))

